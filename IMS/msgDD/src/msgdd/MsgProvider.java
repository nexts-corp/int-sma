public class MsgProvider {
    String nxServerName;
    String nxUserName;
    String nxPassword;
    String nxText;
    String nxTo;
    String nxSender;

    public String getNxServerName() {
        return nxServerName;
    }

    public void setNxServerName(String nxServerName) {
        this.nxServerName = nxServerName;
    }

    public String getNxUserName() {
        return nxUserName;
    }

    public void setNxUserName(String nxUserName) {
        this.nxUserName = nxUserName;
    }

    public String getNxPassword() {
        return nxPassword;
    }

    public void setNxPassword(String nxPassword) {
        this.nxPassword = nxPassword;
    }

    public String getNxText() {
        return nxText;
    }

    public void setNxText(String nxText) {
        this.nxText = nxText;
    }

    public String getNxTo() {
        return nxTo;
    }

    public void setNxTo(String nxTo) {
        this.nxTo = nxTo;
    }

    public String getNxSender() {
        return nxSender;
    }

    public void setNxSender(String nxSender) {
        this.nxSender = nxSender;
    }
    
    
}
